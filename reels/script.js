  function instareels(){
    let sum = ""
    reels.forEach(function(elem,idx){
      sum+= `<div class="reels">
      <div class="img">
      <video loop autoplay ${elem.ismuted ? 'muted':''} src=${elem.video} ></video>
      <div class="muted" id="${idx}">
      ${elem.ismuted?'<i class="ri-volume-mute-fill"></i>':'<i class="ri-volume-up-fill"></i>'}
      </div>
      </div>
      
      <div class="toppart">
          <h1>Reels</h1>
          <h2>Friends</h2>
      </div>
      <div class="sideicons">
          <div class="mainlike" id="${idx}">
        <h4> 
         ${elem.isLiked? '<i class="ri-heart-3-fill" id="red"></i>':'<i class="ri-heart-3-line"></i>'} 
        </h4>
              <h1>${elem.likeCount}</h1>
          </div>
          <div class="maincomment" id="${idx}">
          <h4> <i class="ri-chat-3-line"></i></h4>
              <h1>${elem.commentCount}</h1>
          </div>
          <div class="mainshare">
          <h4>  <i class="ri-send-plane-line"></i></h4>
              <h1>${elem.shareCount}</h1>
          </div>
          <i class="ri-more-2-line"></i>
          <img src="${elem.profilePic}" alt="">
      </div>
      <div class="profile">
         <div class="name">
          <img src="${elem.profilePic}" alt="">
          <h1>${elem.username}</h1>
          <button class="follow" id=${idx}>${elem.isFollowed?'Unfollow':'Follow'}</button>
         </div> 
         <h2>${elem.description}</h2>
      </div>
      
  </div>`
  
  })
  allreels.innerHTML = sum 
  }
  instareels()

  allreels.addEventListener('click',function(dets){
    
    if(dets.target.className == 'mainlike'){
      if(!reels[dets.target.id].isLiked){
        reels[dets.target.id].likeCount++
      reels[dets.target.id].isLiked = true
      }else{
        reels[dets.target.id].likeCount--
      reels[dets.target.id].isLiked = false
      }
      instareels()
    }
    
  
    if(dets.target.className == 'maincomment'){
      if(reels[dets.target.id].commentCount){
        reels[dets.target.id].commentCount++
      }else{
        reels[dets.target.id].commentCount--
      }
      instareels()
    }
   
    
    if(dets.target.className == 'follow'){
      if(!reels[dets.target.id].isFollowed){
      reels[dets.target.id].isFollowed = true
      }else{
      reels[dets.target.id].isFollowed = false
      }
      instareels()
    }
    

    if(dets.target.className == 'muted'){
         if(!reels[dets.target.id].ismuted){
            reels[dets.target.id].ismuted = true
         }else{
          reels[dets.target.id].ismuted = false
         }
         instareels()
    }
    

  })

 

